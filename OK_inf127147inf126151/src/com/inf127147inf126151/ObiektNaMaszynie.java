public abstract class ObiektNaMaszynie{
    private int czasStartu;
    private int czasTrwania;
    private int index;

    public int getCzasStartu() {
        return czasStartu;
    }

    public int getCzasTrwania() {
        return czasTrwania;
    }

    public int getIndex(){
        return index;
    }

}
